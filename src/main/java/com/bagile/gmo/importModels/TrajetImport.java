public class TrajetImport {


    private String trajet_code;
    private String trajet_villeSource;
    private String trajet_villeDistination;


    public String getTrajet_code() {
        return trajet_code;
    }

    public void setTrajet_code(String trajet_code) {
        this.trajet_code = trajet_code;
    }

    public String getTrajet_villeSource() {
        return trajet_villeSource;
    }

    public void setTrajet_villeSource(String trajet_villeSource) {
        this.trajet_villeSource = trajet_villeSource;
    }

    public String getTrajet_villeDistination() {
        return trajet_villeDistination;
    }

    public void setTrajet_villeDistination(String trajet_villeDistination) {
        this.trajet_villeDistination = trajet_villeDistination;
    }
}
